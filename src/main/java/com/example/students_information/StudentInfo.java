package com.example.students_information;

public class StudentInfo {
    private String stName;
    private String stID;
    private String stAge;
    private String stClass;
    private String stDoB;
    private String stMathM;
    private String stScienceM;
    private String stEngM;
    private String stMathF;
    private String stScienceF;
    private String stEngF;


    public StudentInfo(String stName, String stID, String stAge, String stClass, String stDoB, String stMathM, String stScienceM, String stEngM, String stMathF, String stScienceF, String stEngF) {
        this.stName = stName;
        this.stID = stID;
        this.stAge = stAge;
        this.stClass = stClass;
        this.stDoB = stDoB;
        this.stMathM = stMathM;
        this.stScienceM = stScienceM;
        this.stEngM = stEngM;
        this.stMathF = stMathF;
        this.stScienceF = stScienceF;
        this.stEngF = stEngF;
    }

    public String getStMathM() {
        return stMathM;
    }

    public void setStMathM(String stMathM) {
        this.stMathM = stMathM;
    }

    public String getStScienceM() {
        return stScienceM;
    }

    public void setStScienceM(String stScienceM) {
        this.stScienceM = stScienceM;
    }

    public String getStEngM() {
        return stEngM;
    }

    public void setStEngM(String stEngM) {
        this.stEngM = stEngM;
    }

    public String getStMathF() {
        return stMathF;
    }

    public void setStMathF(String stMathF) {
        this.stMathF = stMathF;
    }

    public String getStScienceF() {
        return stScienceF;
    }

    public void setStScienceF(String stScienceF) {
        this.stScienceF = stScienceF;
    }

    public String getStEngF() {
        return stEngF;
    }

    public void setStEngF(String stEngF) {
        this.stEngF = stEngF;
    }

    public String getStName() {
        return stName;
    }

    public String getStID() {
        return stID;
    }

    public String getStAge() {
        return stAge;
    }

    public String getStClass() {
        return stClass;
    }

    public String getStDoB() {
        return stDoB;
    }

    public void setStName(String stName) {
        this.stName = stName;
    }

    public void setStID(String stID) {
        this.stID = stID;
    }

    public void setStAge(String stAge) {
        this.stAge = stAge;
    }

    public void setStClass(String stClass) {
        this.stClass = stClass;
    }

    public void setStDoB(String stDoB) {
        this.stDoB = stDoB;
    }
}
